// Function to get CoinGecko API URL based on token name and symbol
function getCoinGeckoApiUrl(name, symbol) {
  const coinGeckoBaseUrl = "https://api.coingecko.com/api/v3/simple/price";
  const ids = {
    "Kreator Tether": "tether",
    "Kreator DAI": "dai",
    "Kreator Bitcoin": "bitcoin",
    "Kreator Yearn Finance": "yfi",
    "Kreator Frax": "frax",
    "Kreator Circle": "usd-coin",
    "Kreator Ethereum": "ethereum",
    "Kreator Curve": "crv",
  };

  const params = new URLSearchParams({
    ids: ids[name],
    vs_currencies: "inr",
  });

  return `${coinGeckoBaseUrl}?${params.toString()}`;
}
